# Program do dodawania dwóch liczb

# Pobranie pierwszej liczby od użytkownika
liczba_1 = float(input("Podaj pierwszą liczbę: "))

# Pobranie drugiej liczby od użytkownika
liczba_2 = float(input("Podaj drugą liczbę: "))

# Obliczanie sumy
suma = liczba_1 + liczba_2

# Wyświetlanie wyniku
print(f"Suma {liczba_1} i {liczba_2} wynosi {suma}")